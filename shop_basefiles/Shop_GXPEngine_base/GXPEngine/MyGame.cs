using GXPEngine;
using States;

public class MyGame : Game
{
	private ShopBrowseState shopBrowseState;

	//------------------------------------------------------------------------------------------------------------------------
	//                                                  MyGame()
	//------------------------------------------------------------------------------------------------------------------------        
	public MyGame() : base(800, 600, false)
	{
		shopBrowseState = new ShopBrowseState();
		AddChild(shopBrowseState);
	}

	//------------------------------------------------------------------------------------------------------------------------
	//                                                  Update()
	//------------------------------------------------------------------------------------------------------------------------        
	private void Update()
	{
		shopBrowseState.Step();
	}

	//------------------------------------------------------------------------------------------------------------------------
	//                                                  Main()
	//------------------------------------------------------------------------------------------------------------------------        
	private static void Main()
	{
		new MyGame().Start();
	}
}